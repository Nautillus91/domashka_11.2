def upper_txt(txt):
    """Функция, которая возвращает все заглавными буквами """
    return txt.upper()
result = upper_txt("hello world")
print(result)

def title_txt(txt):
    """функция, которая делает заглавными первые буквы каждого слова в строке"""
    return txt.title()
result = title_txt("hello world")
print(result)